// built IN method on javascript
String.prototype.charAt()
// Returns the character at the specified index.

String.prototype.charCodeAt()
// Returns the Unicode value of the character at the specified index.

String.prototype.concat()
// Combines the text of two or more strings and returns a new string.

String.prototype.endsWith()
// Determines whether a string ends with the characters of a specified string.

String.prototype.includes()
// Determines whether a string contains the characters of a specified string.

String.prototype.indexOf()
// Returns the index within the calling string object of the first occurrence of the specified value, starting the search at fromIndex. Returns -1 if the value is not found.

String.prototype.lastIndexOf()
// Returns the index within the calling string object of the last occurrence of the specified value, or -1 if not found. The calling string is searched backward, starting at fromIndex.

String.prototype.localeCompare()
// Returns a number indicating whether a reference string comes before or after or is the same as the given string in sort order.

String.prototype.match()
// Returns an array of the matches, if any, of the regexp in the string.

String.prototype.normalize()
// Returns the Unicode Normalization Form of the calling string value.

String.prototype.padEnd()
// Pads the current string with a given string (possibly repeated) so that the resulting string reaches a given length.

String.prototype.padStart()
// Pads the current string with a given string (possibly repeated) so that the resulting string reaches a given length.

String.prototype.repeat()
// Returns a new string with a specified number of copies of the string it was called on.

String.prototype.replace()
// Returns a new string with some or all matches of a pattern replaced by a replacement. The pattern can be a string or a regex, and the replacement can be a string or a function to be called for each match.

String.prototype.search()
// Executes the search for a match between a regular expression and this String object.

String.prototype.slice()
// Extracts a section of a string and returns a new string.

String.prototype.split()
// Splits a String object into an array of strings by separating the string into substrings.

String.prototype.startsWith()
// Determines whether a string begins with the characters of a specified string.

String.prototype.substring()
// Returns the part of the string between the start and end indexes, or to the end of the string.

String.prototype.toLowerCase()
// Returns the calling string value converted to lowercase.

String.prototype.toUpperCase()
// Returns the calling string value converted to uppercase.

String.prototype.trim()
// Removes whitespace from both ends of a string.

String.prototype.trimEnd()
// Removes whitespace from the end of a string.

String.prototype.trimStart()
// Removes whitespace from the beginning of a string.